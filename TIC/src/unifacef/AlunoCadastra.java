    private void CadastraButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastraButtonActionPerformed
        Aluno aluno = new Aluno();
        aluno.setNomeAluno(NomeTextField.getText());
        if(MasculinoRadioButton.isSelected()){
            aluno.setSexoAluno("Masculino");
        }else{
            aluno.setSexoAluno("Feminino");
        }
        
        aluno.setEstadoAluno((String)EstadojComboBox.getSelectedItem());
        
        //insere aluno no banco de dados
        AlunoDAOImpl crud = new AlunoDAOImpl();
        if(crud.insere(aluno)){
            JOptionPane.showMessageDialog(null, "Aluno inserido com sucesso!");
        }else {
            JOptionPane.showMessageDialog(null, "Aluno n찾o inserido!");
        }
        
    }//GEN-LAST:event_CadastraButtonActionPerformed

